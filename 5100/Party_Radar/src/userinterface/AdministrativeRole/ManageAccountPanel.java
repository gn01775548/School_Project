    public void popGroupComboBox() {
        cbGroup.removeAllItems();

        for (Group group : enterprise.getGroupDirectory().getGroupList()) {
            cbGroup.addItem(group);
        }
    }
    
    public void populateMemberComboBox(Group group){
        cbName.removeAllItems();
        
        for (Member member : group.getMemberDirectory().getMemberList()){
            cbName.addItem(member);
        }
    }
    
    private void populateRoleComboBox(Group group){
        cbRole.removeAllItems();
        for (Role role : group.getSupportedRole()){
            cbRole.addItem(role);
        }
    }

    public void popData() {

        DefaultTableModel model = (DefaultTableModel) btlUser.getModel();

        model.setRowCount(0);

        for (Group group : enterprise.getGroupDirectory().getGroupList()) {
            for (UserAccount ua : group.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[2];
                row[0] = ua.getUsername();
                row[1] = ua.getRole();
                ((DefaultTableModel) btlUser.getModel()).addRow(row);
            }
        }
    }

