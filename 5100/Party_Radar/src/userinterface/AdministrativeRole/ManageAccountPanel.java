    private void btnCreatAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreatAccountActionPerformed
        String userName = txtAccountName.getText();
        String password = txtPassword.getText();
        if (userName.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Can not be empty!");
            return;
        }
        Group group = (Group) cbGroup.getSelectedItem();
        Member member = (Member) cbName.getSelectedItem();
        Role role = (Role) cbRole.getSelectedItem();
        for (UserAccount ua : group.getUserAccountDirectory().getUserAccountList()){
            if (ua.getUsername().equals(userName)){
                JOptionPane.showMessageDialog(null, "Already created!");
                return;
            }
        }

//       UserAccount u = 
        group.getUserAccountDirectory().createUserAccount(userName, password, member, role);
        
//        //Rita: relate member and userAccount
//        member.setAccount(u);
        

        popData();
        
        txtAccountName.setText(null);
        txtPassword.setText(null);
        JOptionPane.showMessageDialog(null, "Created successfully!");
    }//GEN-LAST:event_btnCreatAccountActionPerformed

