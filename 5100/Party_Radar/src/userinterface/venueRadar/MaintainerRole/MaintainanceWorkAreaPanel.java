    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblMaintainanceRequest.getModel();
        model.setRowCount(0);
        
        
        
        for (InformationPost post : group.getInfoQueue().getInfoPostList()){
            Object[] row = new Object[5];
            row[0] = post;
            row[1] = post.getPlace();
            row[2] = post.getSender().getMember().getName();
            row[3] = post.getReceiver()== null ? null : post.getReceiver().getMember().getName();
            row[4] = post.getStatus();
            
            model.addRow(row);
        }
    }

