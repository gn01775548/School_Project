    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed

        int selectedRow = tblMaintainanceRequest.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a request message to process.");
        }
        
        InformationPost post = (InformationPost)tblMaintainanceRequest.getValueAt(selectedRow, 0);
        post.setReceiver(account);
        post.setStatus("Assigned");
        populateTable();

    }//GEN-LAST:event_btnAssignActionPerformed

    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed

        int selectedRow = tblMaintainanceRequest.getSelectedRow();

        if (selectedRow >= 0) {
            MaintainanceRequestPost post = (MaintainanceRequestPost) tblMaintainanceRequest.getValueAt(selectedRow, 0);

            post.setStatus("Processing");

            ProcessMaintainanceRequestPanel processPanel = new ProcessMaintainanceRequestPanel(userProcessContainer, post, enterprise);
            userProcessContainer.add("processWorkRequestJPanel", processPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        } else {
            JOptionPane.showMessageDialog(null, "Please select a request message to process.");
            return;
        }
    }//GEN-LAST:event_btnProcessActionPerformed

