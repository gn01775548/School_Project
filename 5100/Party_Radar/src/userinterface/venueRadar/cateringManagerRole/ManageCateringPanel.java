    public void refreshTable(){
        DefaultTableModel model = (DefaultTableModel)tblVenue.getModel();
        model.setRowCount(0);
        for(CateringMenu menu: ((VenueRadarEnterprise)enterprise).getMenuDirectory().getMenuDirectory()){        
            Object row[] = new Object[2];
            row[0] = menu;
            row[1] = menu.getCateringName();
            model.addRow(row);
        }   
    }

    
