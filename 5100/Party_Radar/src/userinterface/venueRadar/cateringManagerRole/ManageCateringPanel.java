    private void btnDeleteVenueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteVenueActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblVenue.getSelectedRow();       
        if(selectedRow>=0) {
            int i = JOptionPane.showConfirmDialog(null, "Are you sure to delete this catering?");
            if (i == JOptionPane.YES_OPTION){
                CateringMenu menu = (CateringMenu) tblVenue.getValueAt(selectedRow, 0);
                ((VenueRadarEnterprise)enterprise).getMenuDirectory().removeCateringMenu(menu);
                        //.getVenueDirectory().removeVenue(venue);
                refreshTable();
            }
        else{
                JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteVenueActionPerformed
    
