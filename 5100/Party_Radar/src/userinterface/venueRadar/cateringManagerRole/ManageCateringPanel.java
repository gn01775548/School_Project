    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed

        
        int selectRow =tblVenue.getSelectedRow();
        if(selectRow<0) {
            JOptionPane.showMessageDialog(null,"Please selecte a row");
        }
        else{
            CateringMenu menu = (CateringMenu) tblVenue.getValueAt(selectRow, 0);
            ViewMenuDetailsPanel vmPanel = new ViewMenuDetailsPanel (userProcessContainer, enterprise, menu);
            userProcessContainer.add("ViewMenuDetailsPanel",vmPanel);
            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
            cardLayout.next(userProcessContainer);
        }
        
    }//GEN-LAST:event_btnViewDetailsActionPerformed

