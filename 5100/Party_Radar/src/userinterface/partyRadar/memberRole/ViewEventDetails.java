    private void populateAttendees(EventPost post){
        
        DefaultTableModel model = (DefaultTableModel) tblAttandees.getModel();
        model.setRowCount(0);
        
        for (UserAccount u: post.getCurrentRegister()){
           Object[] row = new Object[1];
           row[0] = u.getUsername();
           model.addRow(row);
       }

    }
    
    private void changeBtnTxt(){
        if (userAccount.favoriteEventPostContains(post)){
            btnFavorite.setText("Cancel favorite");
        } else {
            btnFavorite.setText("Favorite");
        }
        
        if (userAccount.likeEventPostContains(post)){
            btnLike.setText("Liked");
        } else {
            btnLike.setText("Like");
        }
    }
    

