    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
        
        Component[] components = userProcessContainer.getComponents();
        for (Component c: components){
            if (c instanceof BrowseEvents){
                BrowseEvents panel = (BrowseEvents)c;
                panel.populateEvent();
                panel.populateHotEvent();
            }
        }

    }//GEN-LAST:event_btnBackActionPerformed

    private void btnAttendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttendActionPerformed

         if(userAccount.getInfoQueue().getInfoPostList().indexOf(post)<0){
            if (post.attend()){
                // Rita: add post to user's infoQueue
                userAccount.getInfoQueue().getInfoPostList().add(post);
                // Rita: add user to eventPost currentRegister
                post.addCurrentRegister(userAccount);
            } else {
                JOptionPane.showMessageDialog(null, "No remaining location!");
            }
            JOptionPane.showMessageDialog(null, "Registered successfully!");
            }
        else{
            JOptionPane.showMessageDialog(null, "You already registered.");      
        } 
         populateAttendees(post);
    }//GEN-LAST:event_btnAttendActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        if(userAccount.getInfoQueue().getInfoPostList().indexOf(post)<0){               
            JOptionPane.showMessageDialog(null, "You have not registered yet!");
        }
        else{
            if (post.cancelAttend()){
                userAccount.getInfoQueue().getInfoPostList().remove(post);
                post.removeCurrentRegister(userAccount);
                JOptionPane.showMessageDialog(null, "Canceled successfully!");
                populateAttendees(post);
            } else {
                JOptionPane.showMessageDialog(null, "Cancel failure!");
            }
        }      
    }//GEN-LAST:event_btnCancelActionPerformed

    private void btnFavoriteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFavoriteActionPerformed

        if (userAccount.favoriteEventPostContains(post)){
            if (userAccount.removeFavoriteEventPost(post)){
                post.reduceFavorites();
                JOptionPane.showMessageDialog(null, "Successfully canceled!");
            } else {
                JOptionPane.showMessageDialog(null, "Not saved!");
            }
        } else {
            if (userAccount.addFavoriteEventPost(this.post)){
                post.addFavorites();
                JOptionPane.showMessageDialog(null, "Succesfully saved!");
            } else {
                JOptionPane.showMessageDialog(null, "Saved!");
            }
        }
        changeBtnTxt();
    }//GEN-LAST:event_btnFavoriteActionPerformed

    private void btnLikeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLikeActionPerformed
        if (userAccount.likeEventPostContains(post)){
            if (userAccount.removeLikeEventPost(post)){
                post.reduceLikes();
                JOptionPane.showMessageDialog(null, "Successfully canceled!");
            } else {
                JOptionPane.showMessageDialog(null, "Not liked!");
            }
        } else {
            if (userAccount.addLikeEventPost(post)){
                post.addLikes();
                JOptionPane.showMessageDialog(null, "Like success!");
            } else {
                JOptionPane.showMessageDialog(null, "Liked!");
            }
        }
        changeBtnTxt();
    }//GEN-LAST:event_btnLikeActionPerformed

    
