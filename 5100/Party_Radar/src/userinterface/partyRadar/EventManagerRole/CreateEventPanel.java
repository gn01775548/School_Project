    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        try{   
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
//            int attendees = Integer.parseInt(txtNumberAttendees.getText());
            dateFormat.setLenient(false);

            String eventName = txtEventName.getText();
            String date = txtDate.getText();

            if (eventName.isEmpty()||date.isEmpty()){
                JOptionPane.showMessageDialog(null, "Please add all the mandatory fields!");
                } 
            else {   
                Date eventDate = dateFormat.parse(date); 
                Date now = new Date();
                if (eventDate.getTime() < now.getTime()) {
                    JOptionPane.showMessageDialog(null, "Time cannot be pastï¼");
                    return;
                }
                InformationPost ePost = new EventPost();
                ePost.setMessage(eventName);
                ((EventPost) ePost).setEventName(eventName);
                ePost.setDate(eventDate);
                ePost.setSender(account);
                ePost.setStatus("Event Created");
//                ((EventPost)ePost).setPicPath(lblPicPath.getText());
                ((EventPost)ePost).setNumberAttendees(Integer.parseInt(jSpinner1.getValue().toString()));
                ((EventPost)ePost).setAvailableSpot(Integer.parseInt(jSpinner1.getValue().toString()));
                ((EventPost)ePost).setDetails(txtAreaEventDetails.getText());
//                ((EventPost) post).setStatus("Created");
//                ((EventPost) post).setNumberAttendees(attendees);


                Group gro = null;
                for (Group group : enterprise.getGroupDirectory().getGroupList()){
                    if (group instanceof EventManagerGroup){
                        gro = group;
                        break;
                    }
                }
                if (gro!=null){
                    gro.getInfoQueue().getInfoPostList().add(ePost);
                    account.getInfoQueue().getInfoPostList().add(ePost);
                }
                JOptionPane.showMessageDialog(null, "Event successfully created!");
                
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                userProcessContainer.add("BrowseVenuePanel", new BrowseVenuePanel(userProcessContainer,account, group, enterprise, campus, txtDate.getText(),ePost));
                layout.next(userProcessContainer);
            }
        }
        catch(ParseException e){
            JOptionPane.showMessageDialog(null, "Please follow valide date format!");
        }

        
        
        //txtVenue.setText(null);
//        txtNumberAttendees.setText(null);
        //txtAreaEventDetails.setText(null);
        
    }//GEN-LAST:event_btnNextActionPerformed

