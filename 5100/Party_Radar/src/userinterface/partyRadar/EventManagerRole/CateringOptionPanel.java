    private void btnBook1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBook1ActionPerformed
            int selectedRow = tblVenue.getSelectedRow();
        try{            
            if (selectedRow < 0){
                JOptionPane.showMessageDialog(null, "Please select a row to process!");}
            else{
                InformationPost cPost = new CateringRequestPost();
                CateringMenu m = (CateringMenu) tblVenue.getValueAt(selectedRow,0);

                SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                dateFormat.setLenient(false);

                Date nDate = dateFormat.parse(date);

                ((CateringRequestPost)cPost).setCateringMenu(m);
                cPost.setSender(account);
                cPost.setStatus("Catering Requested");
                cPost.setDate(nDate);
                cPost.setMessage(m.getCateringName());
                ((EventPost)ePost).setcPost((CateringRequestPost)cPost);
                
                Group gro = null;
                for (Enterprise e : campus.getEnterpriseDirectory().getEnterpriseList()){
                    for (Group group: e.getGroupDirectory().getGroupList()){                
                        if (group instanceof CateringManagerGroup){
                            gro = group;
                            break;
                        }
                    }
                if (gro!=null){          
                    gro.getInfoQueue().getInfoPostList().add(cPost);
//                    account.getInfoQueue().getInfoPostList().add(vPost);               
                    }            
                }
                JOptionPane.showMessageDialog(null, "Catering booking request sent!");
            }
        }
        catch(ParseException e){
            JOptionPane.showMessageDialog(null, "Please follow valide date format!");
        }
    }//GEN-LAST:event_btnBook1ActionPerformed

