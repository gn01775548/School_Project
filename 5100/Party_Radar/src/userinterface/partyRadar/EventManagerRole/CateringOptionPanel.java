    public void refreshTable(){
        
        DefaultTableModel model = (DefaultTableModel)tblVenue.getModel();
        model.setRowCount(0);
        
       ArrayList<Enterprise> enterpriseList =  campus.getEnterpriseDirectory().getEnterpriseList();
       for (Enterprise e: enterpriseList){
           if (e instanceof VenueRadarEnterprise){
               ArrayList<CateringMenu> menuList = ((VenueRadarEnterprise )e).getMenuDirectory().getMenuDirectory();
               for (CateringMenu m: menuList){
                    Object[] row = new Object[3];
                    row[0] = m;
                    row[1] = m.getCateringName();//getVenueName();
                    row[2] = m.getType();//getLabel();
                    model.addRow(row);
               }
           }
       }
    }

    

