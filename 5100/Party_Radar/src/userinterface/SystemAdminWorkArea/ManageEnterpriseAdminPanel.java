    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblEnterprise.getModel();

        model.setRowCount(0);
        for (Campus campus : system.getCampusList()) {
            for (Enterprise enterprise : campus.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount userAccount: enterprise.getUserAccountDirectory().getUserAccountList()){
                Object[] row = new Object[3];
                row[0] = enterprise.getName();
                row[1] = campus.getName();
                row[2] = userAccount.getUsername();

                model.addRow(row);
                }
            }
        }
    }
    
    private void populateCampusComboBox(){
        cbCampus.removeAllItems();
        
        for (Campus campus: system.getCampusList()){
            cbCampus.addItem(campus);
        }
    }
    
    private void populateEnterpriseComboBox(Campus campus){
        cbEnterpriseType.removeAllItems();
        
        for (Enterprise enterprise : campus.getEnterpriseDirectory().getEnterpriseList()){
            cbEnterpriseType.addItem(enterprise);
        }
        
    }
    
