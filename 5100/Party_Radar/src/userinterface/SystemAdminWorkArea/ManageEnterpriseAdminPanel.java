    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed

        Enterprise enterprise = (Enterprise) cbEnterpriseType.getSelectedItem();
        
        String username = txtUserName.getText();
        String password = String.valueOf(pswdField.getPassword());
        String name = txtName.getText();
        Member member = enterprise.getMemberDirectory().createMember(name);
        if (username.isEmpty() || password.isEmpty() || name.isEmpty()){
            JOptionPane.showMessageDialog(null, "Fill in completely!");
            return;
        }
        for (UserAccount ua : enterprise.getUserAccountDirectory().getUserAccountList()){
            if (ua.getUsername().equals(username)){
                JOptionPane.showMessageDialog(null, "Already created!");
                return;
            }
        }
        UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, member, new AdminRole());

        populateTable();
        txtUserName.setText(null);
        pswdField.setText(null);
        txtName.setText(null);
        JOptionPane.showMessageDialog(null, "Created successfully!");
    }//GEN-LAST:event_btnCreateActionPerformed

