       chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
       int c = chooser.showOpenDialog(null);
       if(c== chooser.APPROVE_OPTION){
           pic= chooser.getSelectedFile().getAbsolutePath();
           String name= chooser.getSelectedFile().getName();
           System.out.println("Current File Path"+pic+";\n:Current File Name"+name);
       }
       
       else{
           System.out.println("Don't have file");
       }
       
       ImageIcon icon=new ImageIcon(pic);
       icon.setImage(icon.getImage().getScaledInstance(160,123,Image.SCALE_DEFAULT));
       picLabel.setIcon(icon);
      

