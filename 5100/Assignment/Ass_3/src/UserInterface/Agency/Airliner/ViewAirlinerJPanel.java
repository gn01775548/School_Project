    private void refreshTable() {
        int rowCount = flightCatalog.getRowCount();
        DefaultTableModel model = (DefaultTableModel)flightCatalog.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        
        for(Flight f : airliner.getFlightDirectory().getFlightDir()) {
            Object row[] = new Object[5];
            row[0] = f;
            row[1] = f.getFlightTime();
            row[2] = f.getFlightFromLoca();
            row[3] = f.getFlightToLoca();
            row[4] = f.getAirplaneName();
            model.addRow(row);
        }
    }

