    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if(!passwordPatternCorrect()){
            JOptionPane.showMessageDialog(null,"Password must contain alphanumeric values and '+', '_', and '$' only");
        }       
        
        else if (!txtPword.getText().equals(txtRePword.getText())){
            txtRePword.setForeground(Color.RED);
            JOptionPane.showMessageDialog(null, "Password are not consistent!");
        }
        
        else if (!usernamePatternCorrect()){
            JOptionPane.showMessageDialog(null, "Username cannot start with '_' and can contain alphanumeric values and â€˜_', '.', and '@' only");
        }
                    
        else if (!txtPword.getText().equals(txtRePword.getText())){
            JOptionPane.showMessageDialog(null, "Password are not consistent!");
        }
        
        else if (btnGroup.isSelected(radioCustomer.getModel())){
            Customer customer = new Customer(new Date(),txtPword.getText(),txtUser.getText());
            admin.getCustDir().getCustomerList().add(customer);
            JOptionPane.showMessageDialog(null, "Customer successfully created!");
        }
        else if (btnGroup.isSelected(radioSupplier.getModel())){
            Supplier supplier = new Supplier(txtPword.getText(), txtUser.getText());
            admin.getSuppDir().getSupplierList().add(supplier);
            JOptionPane.showMessageDialog(null,"Supplier successfully created!");
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select the account type!");
               
    }        

        
    }//GEN-LAST:event_btnCreateActionPerformed

