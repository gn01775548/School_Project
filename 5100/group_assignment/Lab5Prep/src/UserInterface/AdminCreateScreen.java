    public boolean passwordPatternCorrect(){
        Pattern p = Pattern.compile("[^A-Za-z0-9@&$]");
        Matcher m = p.matcher(txtPword.getText());
        
        boolean b = m.find();
        if (b == true){
            System.out.println("Illegal password");
            return false;
        }
        else{
            System.out.println("Valid password");
            return true;
        }
    }
    
    public boolean usernamePatternCorrect(){
        Pattern p = Pattern.compile("[^[_]|[^A-Za-z0-9@_.]]");
        Matcher m = p.matcher(txtUser.getText());
        
        boolean b = m.find();
        if (b == true){
            System.out.println("Illegal username");
            return false;
        }
        else{
            System.out.println("Valid username");
            return true;
        }
    }
    
